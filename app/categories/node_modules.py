"""Find node_modules folders and flag likely-orphan ones."""
from __future__ import annotations

import os
import time
from dataclasses import dataclass
from pathlib import Path
from typing import Iterable, List

from ..core.logger import get_logger
from ..core.settings import load_settings
from .base import Category, CleanItem, Risk
from .python_artifacts import SCAN_EXCLUDES, _default_scan_roots

_log = get_logger()

ORPHAN_AGE_SECONDS = 180 * 24 * 3600  # 6 months


@dataclass
class NodeProject:
    project_dir: Path
    node_modules: Path
    has_manifest: bool
    last_modified: float


def _find_node_modules(roots: Iterable[Path], *, max_depth: int = 8) -> List[NodeProject]:
    found: List[NodeProject] = []
    for root in roots:
        if not root.exists():
            continue
        base_depth = len(root.parts)
        try:
            for dirpath, dirnames, _filenames in os.walk(root):
                depth = len(Path(dirpath).parts) - base_depth
                if depth > max_depth:
                    dirnames[:] = []
                    continue
                pruned = []
                keep = []
                for d in dirnames:
                    if d == "node_modules":
                        nm = Path(dirpath) / d
                        project_dir = Path(dirpath)
                        has_manifest = (project_dir / "package.json").exists()
                        try:
                            mtime = project_dir.stat().st_mtime
                        except OSError:
                            mtime = 0.0
                        found.append(
                            NodeProject(
                                project_dir=project_dir,
                                node_modules=nm,
                                has_manifest=has_manifest,
                                last_modified=mtime,
                            )
                        )
                        pruned.append(d)
                    elif d in SCAN_EXCLUDES:
                        pruned.append(d)
                    else:
                        keep.append(d)
                dirnames[:] = keep
        except (PermissionError, OSError) as exc:
            _log.debug("skip %s: %s", root, exc)
    return found


class NodeModulesCategory(Category):
    id = "node_modules"
    name = "node_modules (orphans)"
    description = (
        "node_modules folders found in your project directories. Reinstall with "
        "`npm install` / `pnpm install` when you open the project again."
    )

    def build_items(self) -> Iterable[CleanItem]:
        settings = load_settings()
        roots = _default_scan_roots() + [
            Path(p).expanduser() for p in settings.extra_scan_roots if p
        ]
        roots = [r for r in roots if r.exists() and r.is_dir()]
        if not roots:
            return []

        projects = _find_node_modules(roots)
        if not projects:
            return []

        orphans: List[NodeProject] = []
        recent: List[NodeProject] = []
        now = time.time()
        for p in projects:
            is_orphan = (not p.has_manifest) or (now - p.last_modified > ORPHAN_AGE_SECONDS)
            (orphans if is_orphan else recent).append(p)

        items: List[CleanItem] = []
        if orphans:
            items.append(
                CleanItem(
                    id="nodemod.orphans",
                    name=f"node_modules marked orphan ({len(orphans)})",
                    paths=[o.node_modules for o in orphans],
                    risk=Risk.MEDIUM,
                    affects=(
                        "Projects without package.json or untouched for over 6 months. "
                        "Reinstall with `npm install` if you come back to them."
                    ),
                    reversible=True,
                    needs_extra_allowed=list({o.node_modules.parent for o in orphans}),
                )
            )
        if recent:
            items.append(
                CleanItem(
                    id="nodemod.recent",
                    name=f"node_modules in active projects ({len(recent)})",
                    paths=[r.node_modules for r in recent],
                    risk=Risk.HIGH,
                    affects=(
                        "Recently modified projects. Reinstall needed before running "
                        "builds / dev servers. Selected OFF by default."
                    ),
                    reversible=True,
                    default_selected=False,
                    needs_extra_allowed=list({r.node_modules.parent for r in recent}),
                )
            )
        return items
